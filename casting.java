public class casting {
    public static void main(String[] args) {

        //casting
        // implicit casting

        double price = 100;
        double finalPcice = price + 18.8;
        System.out.println(finalPcice);

        // explicit casting
        
        int p = 100;
        int fP = p + (int)18.4;
        System.out.println(fP);

    }
}
